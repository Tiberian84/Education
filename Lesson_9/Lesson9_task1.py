# Ввод числа N
N = int(input("Введите количество чисел: "))

# Проверка на допустимый диапазон для N
if N < 1 or N > 100000:
    print("Ошибка: количество чисел должно быть в диапазоне от 1 до 100000 включительно.")
else:
    # Ввод чисел с проверкой на ограничение по модулю
    numbers = []
    while len(numbers) < N:
        try:
            # Ввод строки с числами
            input_numbers = list(map(int, input("Введите числа через пробел: ").split()))
            
            # Проверка количества введённых чисел
            if len(input_numbers) != N:
                print(f"Ошибка: нужно ввести ровно {N} чисел. Попробуйте снова.")
                continue
            
            # Проверка каждого числа на ограничение по модулю
            valid = True
            for num in input_numbers:
                if abs(num) > 2 * 10**9:
                    print(f"Ошибка: число {num} превышает 2*10^9 по модулю. Попробуйте снова.")
                    valid = False
                    break
            
            # Если все числа корректны, добавляем их в список
            if valid:
                numbers = input_numbers
        except ValueError:
            print("Ошибка: введите только целые числа.")

    # Используем множество для нахождения уникальных чисел
    unique_numbers = set(numbers)

    # Выводим количество уникальных чисел
    print(len(unique_numbers))
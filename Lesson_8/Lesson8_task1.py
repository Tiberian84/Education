# Ввод числа N
N = int(input("Введите количество чисел: "))

# Проверка на допустимый диапазон значений N
if N < 1 or N > 10000:
    print("Ошибка: количество чисел должно быть в диапазоне от 1 до 10000.")
else:
    # Ввод N чисел и сохранение их в массив
    numbers = []
    for _ in range(N):
        number = int(input("Введите число: "))
        # Проверка на ограничение по модулю (не превышает 10^5)
        if abs(number) > 10**5:
            print("Ошибка: число должно быть по модулю не больше 10^5.")
            exit()  # Прекращаем выполнение программы
        numbers.append(number)

    # Переворачиваем массив
    reversed_numbers = numbers[::-1]

    # Вывод перевёрнутого массива
    print("Перевёрнутый массив:")
    print(" ".join(map(str, reversed_numbers)))
# Ввод чисел A и B
A = int(input("Введите число A: "))
B = int(input("Введите число B: "))

# Проверка гарантии A ≤ B
if A > B:
    print("Ошибка: A должно быть меньше или равно B")
else:
    # Создаем список для хранения четных чисел
    even_numbers = []

    # Перебираем числа от A до B
    for number in range(A, B + 1):
        if number % 2 == 0:  # Проверяем, является ли число четным
            even_numbers.append(str(number))  # Добавляем число в список

    # Выводим четные числа через пробел
    print("Четные числа на отрезке:", " ".join(even_numbers))
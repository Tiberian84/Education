# Запрашиваем у пользователя ввод сторон прямоугольника
width = float(input("Введите ширину прямоугольника: "))
height = float(input("Введите высоту прямоугольника: "))

# Вычисляем площадь и периметр
area = width * height  # Площадь = ширина * высота
perimeter = 2 * (width + height)  # Периметр = 2 * (ширина + высота)

# Выводим результаты
print(f"Площадь прямоугольника: {area}")
print(f"Периметр прямоугольника: {perimeter}")
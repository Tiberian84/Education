# Ввод данных
X = int(input("Минимальная сумма инвестиций: "))
A = int(input("Сумма у Майкла: "))
B = int(input("Сумма у Ивана: "))

# Проверка условий
if A >= X and B >= X:
    # Оба могут вложиться
    result = 2
elif A >= X:
    # Только Майкл может вложиться
    result = "Mike"
elif B >= X:
    # Только Иван может вложиться
    result = "Ivan"
elif A + B >= X:
    # Вместе могут вложиться
    result = 1
else:
    # Никто не может вложиться
    result = 0

# Вывод результата
print(result)
# Создаём пустой словарь pets
pets = {}

# Функция для определения правильного склонения слова "год/года/лет"
def get_age_suffix(age):
    if age % 10 == 1 and age % 100 != 11:
        return "год"
    elif 2 <= age % 10 <= 4 and (age % 100 < 10 or age % 100 >= 20):
        return "года"
    else:
        return "лет"

# Заполняем словарь данными через пользовательский ввод
pet_name = input("Введите имя питомца: ")
pet_info = {
    "Вид питомца": input("Введите вид питомца: "),
    "Возраст питомца": int(input("Введите возраст питомца: ")),
    "Имя владельца": input("Введите имя владельца: ")
}

# Добавляем информацию о питомце в словарь pets
pets[pet_name] = pet_info

# Получаем данные из словаря
for name, info in pets.items():
    age = info["Возраст питомца"]
    age_suffix = get_age_suffix(age)  # Определяем правильное склонение
    print(f"Это {info['Вид питомца']} по кличке \"{name}\". "
          f"Возраст питомца: {age} {age_suffix}. "
          f"Имя владельца: {info['Имя владельца']}")
# Ввод числа X
X = int(input("Введите натуральное число X: "))

# Инициализация счетчика делителей
divisor_count = 0

# Перебор всех чисел от 1 до sqrt(X)
i = 1
while i * i <= X:
    if X % i == 0:  # Если i - делитель числа X
        divisor_count += 1
        if i != X // i:  # Если i и X // i - разные делители
            divisor_count += 1
    i += 1

# Вывод результата
print(f"Количество натуральных делителей числа {X}: {divisor_count}")
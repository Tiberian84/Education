# Ввод максимальной массы лодки
m = int(input("Введите максимальную массу, которую может выдержать одна лодка: "))

# Ввод количества рыбаков
n = int(input("Введите количество рыбаков: "))

# Ввод весов рыбаков с проверкой и зацикливанием
weights = []
for i in range(n):
    while True:
        try:
            weight = int(input(f"Введите вес рыбака {i + 1}: "))
            if 1 <= weight <= m:
                weights.append(weight)
                break  # Выход из цикла при корректном вводе
            else:
                print(f"Ошибка: вес рыбака должен быть в диапазоне от 1 до {m}. Попробуйте снова.")
        except ValueError:
            print("Ошибка: введите целое число.")

# Сортируем веса по возрастанию
weights.sort()

# Жадный алгоритм
left = 0  # Указатель на лёгких рыбаков
right = n - 1  # Указатель на тяжёлых рыбаков
boats = 0  # Счётчик лодок

while left <= right:
    # Если сумма весов двух рыбаков не превышает m
    if weights[left] + weights[right] <= m:
        left += 1  # Лёгкий рыбак садится в лодку
    right -= 1  # Тяжёлый рыбак всегда садится в лодку
    boats += 1  # Увеличиваем счётчик лодок

# Вывод результата
print("Минимальное количество лодок:", boats)